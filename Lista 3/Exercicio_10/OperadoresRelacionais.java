class OperadoresRelacionais{
    public static void main(String[] args) {
        int a = 1;
        int b = 2;
        System.out.print(a > b);
        System.out.print(a >= b);
        System.out.print(a < b);
        System.out.print(a <= b);
        System.out.print(a == b);
        System.out.print(a != b);
    }
}